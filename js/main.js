function show_profile(){


    var img=$("#user_img");
    var b_img=$("#back_imag_id");

img.addClass("img_anim");
b_img.addClass("back_image_anim");



}

function un_show_profile(){


    var img=$("#user_img");
    var b_img=$("#back_imag_id");

img.removeClass("img_anim");
b_img.removeClass("back_image_anim");



}